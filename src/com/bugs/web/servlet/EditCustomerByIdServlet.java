	/**
	 * The doGet method of the servlet. <br>
	 *
	 * This method is called when a form has its tag value method equals to get.
	 * 
	 * @param request the request send by the client to the server
	 * @param response the response send by the server to the client
	 * @throws ServletException if an error occurred
	 * @throws IOException if an error occurred
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		response.setContentType("text/html");
		//PrintWriter out = response.getWriter();
		Map<String,String> map = UploadUtils.HandleFormByUpload(request, getServletContext());
		
		Customer customer=new Customer();
		try {
			BeanUtils.populate(customer, map);
		} catch (IllegalAccessException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (InvocationTargetException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		CustomerService customerService=new CustomerService();
		try {
			if(customerService.updateCustomer(customer)){
				request.getRequestDispatcher("home/EditSuccess.jsp").forward(request, response);
			}else {
				request.getRequestDispatcher("home/EditFail.jsp").forward(request, response);
			}
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

